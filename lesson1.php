    <h2>What Are Roots?</h2>
    <p>The root of a number is a value that, when multiplied by itself a certain number of times, gives the original number. The most common root is the square root (√), but there are also cube roots (∛) and higher.</p>
    
    <h2>Simplify Expressions with Roots</h2>
    <p>To simplify an expression with a root, factor the number under the root and take out perfect squares:</p>
    <ul>
        <li>Example: √18 = √(9 × 2) = 3√2</li>
        <li>Example: √50 = √(25 × 2) = 5√2</li>
    </ul>
    
    <h2>Estimate and Approximate Roots</h2>
    <p>Sometimes, a root does not simplify to a whole number. In such cases, estimate using perfect squares:</p>
    <ul>
        <li>Example: √20 is between √16 (4) and √25 (5), so √20 ≈ 4.5</li>
        <li>Example: √45 is between √36 (6) and √49 (7), so √45 ≈ 6.7</li>
    </ul>
    
    <h2>Simplify Variable Expressions with Roots</h2>
    <p>When dealing with variables, apply the same rules as with numbers:</p>
    <ul>
        <li>Example: √x² = x</li>
        <li>Example: √(x⁴y²) = x²y</li>
        <li>Example: √(x³) = x√x</li>
    </ul>
    
